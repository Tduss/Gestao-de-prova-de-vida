    private void txt_deducacao_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_deducacao_searchKeyReleased
        // TODO add your handling code here:
        try
        {
            String database = "SELECT * FROM staff_info WHERE ID = ?";
            pst = conn.prepareStatement(database);
            pst.setString(1,txt_deducacao_search.getText());
            rs = pst.executeQuery();
            
            
            if (rs.next()) 
                {
                    String add1 = rs.getString("ID");
                    txt_deducao_ID.setText(add1);
                    
                    String add2 = rs.getString("First_Name");
                    txt_deducao_nome.setText(add2);

                    String add3 = rs.getString("Last_Name");
                    txt_deducao_apelido.setText(add3);
                    
                    String add4 = rs.getString("Date_of_birth");
                    txt_deducao_dataNascimento.setText(add4);
                    
                    String add5 = rs.getString("Department");
                    txt_deducao_departamento.setText(add5);

                    String add6 = rs.getString("Salary");
                    txt_deducao_salario.setText(add6);
                    
                    String add7 = rs.getString("Designation");
                    txt_deducao_designacao.setText(add7);
                    
                    String add8 = rs.getString("Status");
                    txt_deducao_status.setText(add8);

                    String add9 = rs.getString("Date_Hired");
                    txt_deducao_dateHired.setText(add9);
                    
                    String add10 = rs.getString("Job_Title");
                    txt_deducao_categoria.setText(add10);
                }
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, "Ocorreu algum erro ao carregar os dados");
        }
        finally
        {
            try
            {
                rs.close();
                pst.close();
            }
            catch (Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_txt_deducacao_searchKeyReleased

