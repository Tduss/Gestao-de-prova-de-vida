    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int salary = Integer.parseInt(txt_deducao_salario.getText());
        if (radio_deducao_percentagem.isSelected() == true)
        {
            int percentage = Integer.parseInt(txt_deducao_percentagem.getText());
            
            int total_percentage_deduction = salary / 100 * percentage;
            String x = String.valueOf(total_percentage_deduction);
            int salario = salary - total_percentage_deduction;
            lbl_total_deduzir.setText(x);
            lbl_salario_deduzido.setText(String.valueOf(salario));
        }
        
        if (radio_deducao_montante.isSelected() == true)
        {
            int amount = Integer.parseInt(txt_deducao_montante.getText());
            
            int total_amount_deduction = salary - amount;
            String x = String.valueOf(amount);
            lbl_total_deduzir.setText(x);
            lbl_salario_deduzido.setText(String.valueOf(total_amount_deduction));
        }
    }//GEN-LAST:event_jButton2ActionPerformed

